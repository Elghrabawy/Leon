function open_bar() {
  let bar = document.getElementById("bar_links");
  if (bar.style.display == "block") bar.style.display = "none";
  else bar.style.display = "block";
}

function hide_links_bar() {
  let bar = document.getElementById("bar_links");
  if (bar.style.display == "block") bar.style.display = "none";
}
